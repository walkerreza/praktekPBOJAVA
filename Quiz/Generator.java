public class Generator {


private int daya;
private int voltase;


public Generator(int daya, int voltase){
    this.daya = daya;
    this.voltase = voltase;
}

public int getdaya(){
    return daya;
}
public void setdaya(int daya){
    this.daya=daya;
}
public int getvoltase(){
    return voltase;
}
public void setvoltase(int voltase){
    this.voltase=voltase;
}

}
